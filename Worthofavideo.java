import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
      Scanner sc=new Scanner(System.in);
      int t=sc.nextInt();
      for(int i=0;i<t;i++){
          int S=sc.nextInt();
          int a=24;
          int b=S*a;
          int c=b*1000;
          System.out.println(c);
      }

	}
}
