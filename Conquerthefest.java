import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int N=sc.nextInt();
		int B=sc.nextInt();
		int c=10*B;
		if(N>=c){
		    System.out.println("YES");
		}
		else
		System.out.println("NO");

	}
}
