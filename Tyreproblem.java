import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int t=sc.nextInt();
		for(int i=0;i<t;i++){
		    int N=sc.nextInt();
		    int M=sc.nextInt();
		    int a=N*2;
		    int b=M*4;
		    int c=a+b;
		    System.out.println(c);
		}

	}
}
