import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
          Scanner sc=new Scanner(System.in);
              int X=sc.nextInt();
              int Y=sc.nextInt();
              int c=2*X;
              if(Y>=c){
                  System.out.println("YES");
              }
              else
              {
                  System.out.println("NO");
              }
          }
	}
