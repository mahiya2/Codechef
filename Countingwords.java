import java.util.*;
class Countingwords
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int t=sc.nextInt();
		for(int i=0;i<t;i++)
		{
		    int N=sc.nextInt();
		    int M=sc.nextInt();
		    int a=N*M;
		    System.out.println(a);
		}
	}
}
